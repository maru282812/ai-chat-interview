# 個人情報漏えい等事案対応手順

【社内マニュアル・対外非公開】

制定日:2026年6月18日

最終更新日:2026年6月18日

【本書類の位置づけ】本書類は、個人情報の保護に関する法律第26条(漏えい等の報告・本人通知義務)に基づき、当方(YOTTO)が、本サービス「アンケでYOTTO」において個人情報の漏えい等事案が発生した場合に行うべき対応手順を定めた社内マニュアルです。漏えい等は発生後すぐの対応が重要であり、本マニュアルは平時から確認し、緊急時にすぐ実行できるようにしてください。

【緊急時のFirst Action】

1. 被害拡大の防止(該当システムの一時停止、パスワード変更、アクセス遮断)

2. 事実関係の保全(ログ、スクリーンショット、関係者からの聞き取り記録)

3. 本マニュアル「3. 報告義務の判定」へ

## 1. 「漏えい等」の定義

個人情報保護法第26条にいう「漏えい等」とは、以下のいずれかをいいます。

- 漏えい:個人データが外部に流出すること(例:メール誤送信、データベース不正アクセス、紛失したUSB)

- 滅失:個人データの内容が失われること(例:ハードディスク故障、誤削除)

- 毀損:個人データの内容が改ざんされ、本来の状態が復元できないこと

これに加えて、「漏えい等のおそれ」がある場合も、本マニュアルに従って対応する。

## 2. 漏えい等を発見した場合の初動(発見後1時間以内)

### 2.1 事実確認

1. いつ、どこで、何が、どの程度漏えいしたかを把握する

2. 発見した経緯(本人からの連絡、委託先からの通知、自社で気づいた等)を確認

3. 漏えいした個人データの種類(LINEユーザーID、回答内容、要配慮個人情報を含むか等)を特定

4. 漏えいした件数(概算でよい)を把握

### 2.2 被害拡大の防止

1. 該当システムの一時停止

2. 漏えい経路の遮断(パスワード変更、APIキー無効化、不正アクセス元のブロック等)

3. 該当ユーザーアカウントの一時停止(該当する場合)

4. 委託先で発生した場合、委託先に即時対応を依頼

### 2.3 証拠保全

1. 関連するログ、スクリーンショット、メール、画面録画等を保存

2. 発見した経緯の聞き取りメモを作成

3. 時系列でのアクション記録を開始(以降、すべての対応を記録)

## 3. 報告義務の判定

個人情報保護委員会への報告及び本人通知は、以下のいずれかに該当する場合に法律上義務付けられています(個情法施行規則第7条)。1件でも該当すれば、報告・通知義務が発生します。

| 漏えい等の類型 | 該当例 | 報告義務 |
| --- | --- | --- |
| ①要配慮個人情報を含む漏えい等 | 病歴、信条等が漏えい | 1件でも該当 |
| ②財産的被害が生じるおそれ | クレジットカード番号、口座番号、e-GIFT交換可能なポイント情報が漏えい | 1件でも該当 |
| ③不正目的・故意による漏えい等 | 外部からの不正アクセス、内部不正、ランサムウェア | 1件でも該当 |
| ④1000人超の漏えい等 | 大規模なデータベース流出 | 1000人超で該当 |

【重要】上記④の「1000人超」は1件ずつのケースではなく総数で判定します。①②③のいずれかに該当すれば、件数に関わらず報告義務が発生します。

当方の事業に即して言うと、以下のような場合は1件でも報告義務が発生する可能性が高いため、慎重に判定してください。

- AIチャット履歴にユーザーが投稿した病歴・精神状態に関する情報が漏えい(①該当)

- 外部からの不正アクセスによる漏えい(③該当)

- 内部不正による漏えい(③該当)

- ランサムウェア・マルウェア感染による漏えい(③該当)

## 4. 報告義務がある場合の対応(発見後3〜5日以内に速報)

速報期限:発見後概ね3〜5日以内

確報期限:発見後30日以内(③不正目的の場合は60日以内)

### 4.1 個人情報保護委員会への速報

1. 個人情報保護委員会のウェブフォームから速報を提出(https://www.ppc.go.jp/personalinfo/legal/leakAction/)

2. 速報事項(分かっている範囲でよい)

- 漏えい等の概要

- 漏えい等が発生した個人データの項目

- 漏えい等が発生したおそれがある本人の数

- 原因

- 二次被害又はそのおそれの有無及びその内容

- 本人への対応の実施状況

- 公表の実施状況

- 再発防止のための措置

- その他参考となる事項

### 4.2 個人情報保護委員会への確報

1. 発見後30日以内(③不正目的の場合は60日以内)に確報を提出

2. 速報時点で不明だった事項を補完し、調査結果及び再発防止策を報告

### 4.3 本人通知

1. 漏えい等の影響を受けるすべての本人(ユーザー)に通知する

2. 通知方法

- LINE公式アカウント「Hibi」を経由したメッセージ送信

- 登録メールアドレスがある場合はメール送信

- 本人連絡先が不明な場合は、本サービス上での公表

3. 通知事項

- 漏えい等が発生した個人データの項目

- 原因

- 二次被害又はそのおそれの有無及びその内容

- 本人が取るべき対応(パスワード変更等)

- お問い合わせ窓口

## 5. 報告義務がない場合の対応(任意対応)

報告義務に該当しない場合でも、以下を実施することを推奨します。

- 該当本人への通知(再発防止のための説明、信頼関係の維持)

- 社内記録の保存

- 再発防止策の検討・実施

## 6. クライアント企業への連絡

漏えいした個人データがクライアント企業のために取得したものを含む場合、当該クライアント企業へ速やかに連絡します。

- クライアント企業の業務委託契約書に記載の連絡窓口を確認

- 発生事案、影響範囲、当方の対応状況を報告

- クライアント企業側で必要な対応(再発防止、本人への補足説明等)を協議

- クライアント企業が個人情報取扱事業者として独自に行うべき対応がある場合は、その旨を伝達

