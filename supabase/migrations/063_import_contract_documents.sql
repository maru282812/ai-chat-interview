-- 063_import_contract_documents.sql
-- Import contract DOCX files into documents/document_versions.
-- DOCX tables are stored as Markdown tables for LIFF/admin rendering.
-- Existing v1.0 documents receive an imported v1.1 to preserve consent history.

BEGIN;

CREATE OR REPLACE FUNCTION import_contract_document(
  p_preferred_document_id UUID,
  p_document_type document_type,
  p_title TEXT,
  p_description TEXT,
  p_is_active BOOLEAN,
  p_is_required_global BOOLEAN,
  p_v10_id UUID,
  p_v11_id UUID,
  p_content TEXT
) RETURNS VOID LANGUAGE plpgsql AS $$
DECLARE
  target_document_id UUID;
  existing_v10_id UUID;
  existing_v10_created_by TEXT;
  imported_v11_id UUID;
  existing_version_count INTEGER;
  selected_version_no TEXT;
  preferred_version_id UUID;
  selected_version_id UUID;
  old_current_version_id UUID;
  ts TIMESTAMPTZ := now();
BEGIN
  SELECT id INTO target_document_id
  FROM documents
  WHERE id = p_preferred_document_id OR title = p_title
  ORDER BY CASE WHEN id = p_preferred_document_id THEN 0 ELSE 1 END, created_at
  LIMIT 1;

  IF target_document_id IS NULL THEN
    target_document_id := p_preferred_document_id;
    INSERT INTO documents (id, document_type, title, description, is_active, is_required_global)
    VALUES (target_document_id, p_document_type, p_title, p_description, p_is_active, p_is_required_global);
  ELSE
    UPDATE documents
    SET document_type = p_document_type,
        title = p_title,
        description = p_description,
        is_active = p_is_active,
        is_required_global = p_is_required_global,
        updated_at = ts
    WHERE id = target_document_id;
  END IF;

  SELECT id INTO imported_v11_id
  FROM document_versions
  WHERE document_id = target_document_id AND version_no = '1.1' AND created_by = 'contract-doc-import'
  LIMIT 1;

  SELECT id, created_by INTO existing_v10_id, existing_v10_created_by
  FROM document_versions
  WHERE document_id = target_document_id AND version_no = '1.0'
  LIMIT 1;

  SELECT count(*) INTO existing_version_count
  FROM document_versions
  WHERE document_id = target_document_id;

  IF imported_v11_id IS NOT NULL THEN
    selected_version_no := '1.1';
    preferred_version_id := p_v11_id;
  ELSIF existing_version_count = 0 OR existing_v10_created_by = 'contract-doc-import' THEN
    selected_version_no := '1.0';
    preferred_version_id := p_v10_id;
  ELSE
    selected_version_no := '1.1';
    preferred_version_id := p_v11_id;
  END IF;

  INSERT INTO document_versions (id, document_id, version_no, content, change_reason, effective_from, created_by)
  VALUES (
    preferred_version_id,
    target_document_id,
    selected_version_no,
    p_content,
    CASE WHEN selected_version_no = '1.0' THEN 'Imported contract document initial version' ELSE 'Imported contract document revision' END,
    ts,
    'contract-doc-import'
  )
  ON CONFLICT (document_id, version_no) DO NOTHING;

  SELECT id INTO selected_version_id
  FROM document_versions
  WHERE document_id = target_document_id AND version_no = selected_version_no
  LIMIT 1;

  SELECT current_version_id INTO old_current_version_id
  FROM documents
  WHERE id = target_document_id;

  IF old_current_version_id IS NOT NULL AND old_current_version_id <> selected_version_id THEN
    UPDATE document_versions
    SET effective_to = ts
    WHERE id = old_current_version_id AND effective_to IS NULL;
  END IF;

  UPDATE document_versions SET effective_to = NULL WHERE id = selected_version_id;
  UPDATE documents SET current_version_id = selected_version_id, updated_at = ts WHERE id = target_document_id;
END;
$$;

